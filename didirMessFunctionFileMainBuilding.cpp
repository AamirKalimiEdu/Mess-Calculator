//-------------------return total amount of Ground Floor-------------------------
long groundFloorMain(int ayon, int karim, int hasinur, int sayeem, int nur, int tarik, int ikbal, int samim){
    long totalGFM = ayon + karim + hasinur + sayeem + nur + tarik + ikbal + samim;
    return totalGFM;
}
//-------------------return total amount of First Floor------------------------
long firstFloorMain(int rafik, int rajon, int amin, int obaidur, int tofaj, int rahi, int sariful, int mota,  int ismail){
    long totalFFM = rafik + rajon + amin + obaidur + tofaj + rahi + sariful + mota + ismail;
    return totalFFM;
}
//------------------return total amount of Second Floor-------------------------
long secondFloorMain(int wahab, int salim, int nazmul, int sahid, int moni, int mehedi, int rakesh, int arafat,int mijanur){
	long totalSFM = wahab + salim + nazmul + sahid + moni + mehedi + rakesh + arafat + mijanur;
	return totalSFM;
}
