/*
***-------------------Didir Mess Home Building Total Amount---------------------------
*/
//-------------------return total amount of Ground Floor-----------------------
long groundFloorHome(int monjur, int chhabed, int saheb, int mostafijur, int sohel, int akramul){
	long totalGFH = monjur + chhabed + saheb + mostafijur + sohel + akramul;
	return totalGFH;
}
