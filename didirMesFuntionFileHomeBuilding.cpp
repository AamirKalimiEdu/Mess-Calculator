//--------------------return total amount of Second Floor--------------------
long secondFloorHome(int aliul, int aamir, int baban, int mir, int masrekul, int badshah, int nana){
	long totalGFH = aliul + aamir + baban + mir + masrekul + badshah + nana;
	return totalGFH;
}
